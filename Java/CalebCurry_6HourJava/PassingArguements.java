public class PassingArguements {

    /*
    An argument and parameter are similar, but not the same.
    argument - what you pass in to a method.@interface
    parameter - is the variable that will store that data.
     */
    public static void main(String[] args) {
        //this will take in the argument passed to it.
        System.out.println("Hello " + args[0]);
    }
}
