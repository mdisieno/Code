import java.util.*;

public class Numbers {
    public static void main(String[] args) {
        int numberOne = 1;
        double doubleOne = -2.0;
        
        //will give back answer as
        System.out.println(doubleOne + numberOne);
        System.out.println(Math.abs(doubleOne));
        System.out.println(Math.pow(numberOne, 2));
        System.out.println(Math.sqrt(36));
    }
}
